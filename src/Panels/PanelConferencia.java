    private void createTable(String[] columnNames){
        DefaultTableModel model;      
        model = new DefaultTableModel(columnNames,0);
        tableConf = new JTable(model);        
        tableConferenciaPanel.setLayout(new BoxLayout(tableConferenciaPanel, BoxLayout.PAGE_AXIS));     
        tableConf.setDefaultEditor(Object.class, null);
    }
    public void update(){
        lojaDB.updateTable(tableConf, tableConferenciaPanel, "Mercadoria", false, null);
    }
